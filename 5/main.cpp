#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "Russian");
    int a, b, c;
    cout << "Введите предполагаемые длинны сторон треугольника через пробел: ";
    if (!(cin >> a >> b >> c)) {
        cout << "Ошибка ввода. Введите три целых числа." << "\n";
        return 1;
    }

    if (a <= 0 || b <= 0 || c <= 0) {
        cout << "Треугольник невозможен: все стороны должны быть положительными." << "\n";
        return 0;
    }

    if (a + b <= c || a + c <= b || b + c <= a) {
        cout << "Треугольник невозможен." << "\n";
        return 0;
    }

    if (a == b && b == c) {
        cout << "Треугольник существует. Тип: равносторонний." << "\n";
    } else if (a == b || a == c || b == c) {
        cout << "Треугольник существует. Тип: равнобедренный." << "\n";
    } else {
        cout << "Треугольник существует. Тип: разносторонний." << "\n";
    }

    return 0;
}